#include "minitalk.h"

t_talk g_talk;

int	acknowledgement()
{
	int i;
	int count;

	i = g_talk.idx;
	count = 0;
	if ( i && i % 8 == 0)
	{
		while (i > g_talk.idx - 8 - 1)
		{
			if (g_talk.buffer[i] == '0')
				count++;
			i--;
		}
		if (count == 8)
		{
			if(kill(g_talk.c_pid, SIGUSR1) == -1)
				ft_printf("Cannot send SIGSUR1 to client\n");
			return (1);
		}
	}
	return (0);
}

void	convert_buff(char *str, int idx)
{
	int	i;
	int	idx_str;

	idx_str = 0;
	i = 0;
	while (i <= idx)
	{
		if (i && i % 8 == 0)
		{
			str[idx_str] = ft_bitoa(&g_talk.buffer[i - 8]);
			idx_str++;
		}
		i++;
	}
	str[idx_str] = '\0';
}

void	concat(char *str)
{
	int		old_len;
	int		new_len;
	char	*new_str;

	old_len = ft_strlen(g_talk.str) + 1;
	new_len = old_len + ft_strlen(str);
	new_str = malloc(sizeof(char) * (new_len + 1));
	new_str[0] = '\0';
	ft_strlcat2(new_str, g_talk.str);
	ft_strlcat2(new_str, str);
	new_str[new_len] = 0;
	free(g_talk.str);
	g_talk.str = new_str;
}

void	manage_buffer(int idx)
{
	char	*str;

	if (!g_talk.str)
	{
		g_talk.str = malloc(sizeof(char));
		g_talk.str[0] = '\0';
	}
	str = malloc(sizeof(char) * (idx / 8) + 1);
	convert_buff(str, idx);
	concat(str);
	free(str);
}

void	flush_str()
{
	int i;
	
	i = ft_printf("%s\n", g_talk.str);
	ft_printf("bytes received: %d\n", i);
	free(g_talk.str);
	g_talk.str = NULL;
}

void	sig_pid(int sig, siginfo_t *info, void *content)
{
	(void)content;
	g_talk.c_pid = info->si_pid;
	if (g_talk.idx < BUFFER_SIZE)
	{
		if (sig == SIGUSR1)
			g_talk.buffer[g_talk.idx] = '1';
		else if (sig == SIGUSR2)
			g_talk.buffer[g_talk.idx] = '0';
		g_talk.idx++;
		usleep(400);
		g_talk.buffer[g_talk.idx + 1] = 0;
		if (kill(g_talk.c_pid, SIGUSR2) == -1)
			ft_printf("Cannot send SIGUSR2 to client\n");
	}
}

int main()
{
	struct sigaction	sa;

	ft_printf("PID: %d\n", getpid());
	sigemptyset(&sa.sa_mask);
	sa.sa_flags = SA_SIGINFO;
	sa.sa_sigaction = &sig_pid;
	sigaction(SIGUSR1, &sa, NULL);
	sigaction(SIGUSR2, &sa, NULL);
	while (1)
	{
		pause();
		if (g_talk.idx >= BUFFER_SIZE)
			manage_buffer(g_talk.idx);
		if (acknowledgement())
		{
			manage_buffer(g_talk.idx);
			flush_str();
			g_talk.idx = 0;
		}
		else if (g_talk.idx >= BUFFER_SIZE)
			g_talk.idx = 0;
	}
	return (0);
}

