#include "minitalk.h"

t_talk g_talk;

int	acknowledgement(int c_pid, int idx)
{
	int i;
	int count;

	i = idx;
	count = 0;
	if ( i && i % 8 == 0)
	{
		while (i > idx - 8 - 1)
		{
			if (g_talk.buffer[i] == '0')
				count++;
			i--;
		}
		if (count == 8)
		{
			if(kill(c_pid, SIGUSR1) == -1)
				exit(1);
			return (1);
		}
	}
	return (0);
}

void	convert_buff(char *str, int idx)
{
	int	i;
	int	idx_str;

	idx_str = 0;
	i = 0;
	while (i <= idx)
	{
		if (i && i % 8 == 0)
		{
			str[idx_str] = ft_bitoa(&g_talk.buffer[i - 8]);
			idx_str++;
		}
		i++;
	}
	str[idx_str] = '\0';
}

void	ft_strlcat2(char *s1, char *s2)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (s1[i])
		i++;
	while (s2[j])
	{
		s1[i + j] = s2[j];
		j++;
	}
	s1[i+j] = 0;
}

void	concat(char *str)
{
	int		old_len;
	int		new_len;
	char	*new_str;

	old_len = ft_strlen(g_talk.str) + 1;
	new_len = old_len + ft_strlen(str);
	/* ft_printf("new len =%d\n", new_len); */
	new_str = malloc(sizeof(char) * (new_len + 1));
	new_str[0] = 0;
	ft_strlcat2(new_str, g_talk.str/* , old_len */);
	ft_strlcat2(new_str, str/* , ft_strlen(str) */);
	new_str[new_len + 1] = 0;
	/* ft_printf("str = %s\n", new_str); */
	free(g_talk.str);
	g_talk.str = new_str;
}

void	manage_buffer(int idx)
{
	char	*str;

	str = malloc(sizeof(char) * (idx / 8) /* 32 */);
	convert_buff(str, idx);
	concat(str);
	free(str);
}

void	flush_str()
{
	ft_printf("%s\n", g_talk.str);
	g_talk.str[0] = 0;
}

void	sig_pid(int sig, siginfo_t *info, void *content)
{
	static int	idx = 0;
	int			c_pid;

	(void)content;
	c_pid = info->si_pid;
	if (idx < BUFFER_SIZE)
	{
		if (sig == SIGUSR1)
		{
			g_talk.buffer[idx] = '1';
			/* ft_printf("%c", g_talk.buffer[idx]); */
		}
		else if (sig == SIGUSR2)
		{
			g_talk.buffer[idx] = '0';
			/* ft_printf("%c", g_talk.buffer[idx]); */
		}
		/* if (idx && (idx + 1) % 8 == 0)
			ft_printf(" "); */
		idx++;
		usleep(100);
		g_talk.buffer[idx + 1] = 0;
		kill(c_pid, SIGUSR2);
	}
	if (idx >= BUFFER_SIZE)
	{
		/* ft_printf("yo"); */
		manage_buffer(idx);
	}
	if (acknowledgement(c_pid, idx))
	{
		manage_buffer(idx);
		flush_str();
		idx = 0;
	}
	else if (idx >= BUFFER_SIZE)
		idx = 0;
}

int main()
{
	struct sigaction	sa;

	g_talk.str = malloc(sizeof(char));
	g_talk.str[0] = '\0';
	ft_printf("PID: %d\n", getpid());
	sigemptyset(&sa.sa_mask);
	sa.sa_flags = SA_SIGINFO;
	sa.sa_sigaction = &sig_pid;
	sigaction(SIGUSR1, &sa, NULL);
	sigaction(SIGUSR2, &sa, NULL);
	while (1)
	{
		pause();
	}
	return (0);
}

